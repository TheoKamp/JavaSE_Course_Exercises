import java.lang.Math;

public class Main {

    public static void main(String[] args) {

        System.out.println("Sin: " + Math.sin(2.0));
        System.out.println("Exp: " + Math.exp(2.0));
        System.out.println("Log: " + Math.log(2.0));
        System.out.println("Sqrt: " + Math.sqrt(5.0));
        System.out.println("Floor: " + Math.floor(5.0));
        System.out.println("Pow: " + Math.pow(5.0, 2));

    }
}
